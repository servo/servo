    def __init__(self, condition, ifTrue, ifFalse):
        kids = [CGIfWrapper(condition, ifTrue),
                CGWrapper(CGIndenter(ifFalse), pre=" else {\n", post="\n}")]
        CGList.__init__(self, kids)


