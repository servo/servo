# @description Fetch latest index.bs
fetch_cddl() {
  local url="https://github.com/w3c/webdriver-bidi/raw/refs/heads/main/index.bs"
  curl -sSL "$url"
}

# @description Remove HTML comments in advance
remove_comments() {
  awk -v RS='<!--' 'NR>1 {sub(/.*?-->/, "")} {print}'
}

# @file latest_cddl.sh
# @brief Fetch the latest CDDL definition from WebDriver BiDi spec.
# @description
#    bash latest_cddl.sh > cddls/webdriver-bidi.cddl

# @description Extract CDDL definitions from index.bs
extract_cddl() {
  awk '
    # only extract content in <pre> and ignore comment
    /<pre class="cddl"/ { in_pre = 1; next } 
    /<\/pre>/ { in_pre = 0; next }
    
    in_pre {
        # skip empty line
        if (/^[ \t]*$/) next;
        
        # when a new rule is encountered
        if (/ = /) {
            # manually add empty line when a new rule is encountered
            print ""
            # record the indent of each rule
            match($0, /^[ \t]+/)
            indent = substr($0, 1, RLENGTH)
        }
        
        # remove the indent of each line in the rule
        # guard in case the indent is incorrect
        if (substr($0, 1, length(indent)) == indent) {
            $0 = substr($0, length(indent) + 1);
        }
        
        print
    }
  '
}

# @description add missing choice to specific cddl rule.
add_missing() {
  awk -v rule="$1" -v branch="$2" '
    # check if we are in target rule
    / = / {
      if ($1 == rule) {
        in_rule = 1
        seen = 0
      } else {
        in_rule = 0
      }
      print
      next
    }
    # if in target rule, check if we have seen the branch
    in_rule {
      if (index($0, branch)) {
        seen = 1
      }
    }
    # if we are the ending line of a rule (the line without trailing slash)
    in_rule && !seen && !/^[[:space:]]*$/ && !/\/[[:space:]]*$/ {
      print $0 " /"
      print "  " branch
      seen = 1
      next
    }
    { print }
  '
}

# @description add preamble and patch info before the text.
prepend_preamble() {
  cat <<EOF
; This Source Code Form is subject to the terms of the Mozilla Public
; License, v. 2.0. If a copy of the MPL was not distributed with this
; file, You can obtain one at https://mozilla.org/MPL/2.0/.
;
; The origin of this CDDL file is:
; https://www.w3.org/TR/webdriver-bidi
;
; This file is automatically generated by ./latest_cddl.sh .
; It is not intended for manual editing.
;
; Additional patches generated by the script:
;
; EmulationResult =/ emulation.SetNetworkConditionsResult
; EmulationResult =/ emulation.SetScreenSettingsOverrideResult
EOF
  cat # from stdin
}

main() {
  fetch_cddl |
    remove_comments |
    extract_cddl |
    add_missing "EmulationResult" "emulation.SetNetworkConditionsResult" |
    add_missing "EmulationResult" "emulation.SetScreenSettingsOverrideResult" |
    prepend_preamble
}

main "$@"
